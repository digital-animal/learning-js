export var add = function(n1,n2){
    return n1+n2;
};
export var sub = function(n1,n2){
    return n1-n2;
};
export var mul = function(n1,n2){
    return n1*n2;
};
export var div = function(n1,n2){
    return n1/n2;
};
export var mod = function(n1,n2){
    return n1%n2;
};

export const pi = 3.141592653589793;
export const e = 2.718281828459045;